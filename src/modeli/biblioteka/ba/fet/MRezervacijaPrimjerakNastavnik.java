package modeli.biblioteka.ba.fet;

public class MRezervacijaPrimjerakNastavnik {

	private int sifRezPrimNast;
	private int sifRezervacija;
	private int sifPrimjerak;
	private int sifNastavnik;
	
	public MRezervacijaPrimjerakNastavnik() {
		this.sifRezPrimNast = -1;
		this.sifRezervacija = -1;
		this.sifPrimjerak = -1;
		this.sifNastavnik = -1;
	}

	public int getSifRezPrimNast() {
		return sifRezPrimNast;
	}

	public void setSifRezPrimNast(int sifRezPrimNast) {
		this.sifRezPrimNast = sifRezPrimNast;
	}

	public int getSifRezervacija() {
		return sifRezervacija;
	}

	public void setSifRezervacija(int sifRezervacija) {
		this.sifRezervacija = sifRezervacija;
	}

	public int getSifPrimjerak() {
		return sifPrimjerak;
	}

	public void setSifPrimjerak(int sifPrimjerak) {
		this.sifPrimjerak = sifPrimjerak;
	}

	public int getSifNastavnik() {
		return sifNastavnik;
	}

	public void setSifNastavnik(int sifNastavnik) {
		this.sifNastavnik = sifNastavnik;
	}
	
	
	
}
