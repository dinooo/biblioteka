package modeli.biblioteka.ba.fet;

public class MVaznostObaveznost {
	
	private int sifVaznObav;
	private int sifVaznost;
	private int sifObaveznost;
	
	public MVaznostObaveznost() {
		this.sifVaznObav = -1;
		this.sifVaznost = -1;
		this.sifObaveznost = -1;
	}

	public int getSifVaznObav() {
		return sifVaznObav;
	}

	public void setSifVaznObav(int sifVaznObav) {
		this.sifVaznObav = sifVaznObav;
	}

	public int getSifVaznost() {
		return sifVaznost;
	}

	public void setSifVaznost(int sifVaznost) {
		this.sifVaznost = sifVaznost;
	}

	public int getSifObaveznost() {
		return sifObaveznost;
	}

	public void setSifObaveznost(int sifObaveznost) {
		this.sifObaveznost = sifObaveznost;
	}


	
}
